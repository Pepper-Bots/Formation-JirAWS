package javacore.chapter01.variable.exercise;

public class VariablesPrimitives {

    public static void main(String[] args) {

        // N° ISBN d'un livre
        long BookNb = 1234567891234L;

        // Nb de pages d'un livre
        short BookPages = 5000;

        // Note moyenne d'un livre
        float AverageRatingBook = 4.5F;

        // Statut d'emprunt d'un livre
        boolean BookAvailable = true;

        // Nb total d'utilisateurs inscrits à la bibliothèque
        int NbUsers = 2000;

        // Solde d'amende d'un utilisateur
        double BalanceFineDue = 50.40;

        // Code unique de l'utilisateur
        int UserId = 123567;
    }
}
